#!/usr/bin/env bash
# Find all wandb offline runs under skywork exp root + the central
# wandb_offline cache, bundle into a single zip.
#
# Usage:
#   bash collect_wandb_skywork.sh                                # default paths
#   bash collect_wandb_skywork.sh /custom/exp/root /custom/out.zip
#
# Defaults:
#   OUT_ZIP:     $HOME/skywork_wandb_<ts>.zip   (NOT /tmp)
#   STAGE_ROOT:  $HOME/.cache/wandb_collect/    (NOT /tmp, auto-cleaned)
#
# Searches:
#   1. <EXP_ROOT>/**/offline-run-*   (runs co-located with exp dir)
#   2. /checkpoints/hongpaul-sandbox/rl-opt/wandb_offline/wandb/offline-run-*
#      AND filters by run NAME containing "SkyOR1" or "Skywork-OR1"
#      (these were generated by 7B script's WANDB_DIR default)
set -euo pipefail

EXP_ROOT="${1:-/checkpoints/hongpaul-sandbox/rl-opt/skywork/Skywork-OR1-R1Distill7B}"
# Default output: under $HOME (persistent, NOT /tmp)
OUT_ZIP="${2:-$HOME/skywork_wandb_$(date +%Y%m%d_%H%M%S).zip}"
# Staging dir: under $HOME/.cache (NOT /tmp), auto-cleaned at exit
STAGE_ROOT="${STAGE_ROOT:-$HOME/.cache/wandb_collect}"
CENTRAL_WANDB="/checkpoints/hongpaul-sandbox/rl-opt/wandb_offline/wandb"

# Match patterns for filtering central wandb_offline (run name field)
NAME_PATTERN='SkyOR1|Skywork-OR1|SkyOR1_R1Distill7B'

log() { echo "[$(date '+%H:%M:%S')] $*" ; }

[ -d "$EXP_ROOT" ] || { echo "ERROR: EXP_ROOT not found: $EXP_ROOT" >&2; exit 1; }

mkdir -p "$STAGE_ROOT"
WORKDIR=$(mktemp -d -p "$STAGE_ROOT" wandb_collect.XXXXXX)
trap "rm -rf $WORKDIR" EXIT

log "EXP_ROOT: $EXP_ROOT"
log "OUT_ZIP : $OUT_ZIP"
log "Scanning ..."

# ---- Pass 1: runs nested under exp dirs ---------------------------------
log "Pass 1: rglob under EXP_ROOT/**/offline-run-*"
EXP_RUNS=()
while IFS= read -r -d '' d; do
    EXP_RUNS+=("$d")
done < <(find "$EXP_ROOT" -type d -name "offline-run-*" -print0 2>/dev/null)
log "  found ${#EXP_RUNS[@]} runs nested under exp dirs"

# ---- Pass 2: runs in central wandb_offline matching pattern -------------
log "Pass 2: central $CENTRAL_WANDB filtered by name pattern '$NAME_PATTERN'"
CENTRAL_RUNS=()
if [ -d "$CENTRAL_WANDB" ]; then
    while IFS= read -r -d '' d; do
        # Read the run name from files/config.yaml or output.log
        run_name=""
        if [ -f "$d/files/config.yaml" ]; then
            run_name=$(grep -m1 -E "^[[:space:]]*name:|^[[:space:]]*experiment_name:" "$d/files/config.yaml" 2>/dev/null | head -1)
        fi
        # Also check files/output.log for the exp name
        log_match=""
        if [ -f "$d/files/output.log" ]; then
            log_match=$(grep -m1 -oE "$NAME_PATTERN" "$d/files/output.log" 2>/dev/null || true)
        fi
        if echo "$run_name $log_match $d" | grep -qE "$NAME_PATTERN"; then
            CENTRAL_RUNS+=("$d")
        fi
    done < <(find "$CENTRAL_WANDB" -maxdepth 1 -type d -name "offline-run-*" -print0 2>/dev/null)
    log "  found ${#CENTRAL_RUNS[@]} runs in central matching pattern"
else
    log "  (central dir not present, skipping)"
fi

TOTAL=$(( ${#EXP_RUNS[@]} + ${#CENTRAL_RUNS[@]} ))
if [ "$TOTAL" = "0" ]; then
    log "WARNING: no wandb runs found anywhere."
    log "Check that training actually wrote wandb data, or pass a different EXP_ROOT."
    exit 0
fi

log "Total runs to bundle: $TOTAL"

# ---- Stage into workdir preserving useful structure ---------------------
log "Staging into $WORKDIR ..."
mkdir -p "$WORKDIR/from_exp_root" "$WORKDIR/from_central"

for run in "${EXP_RUNS[@]}"; do
    # Preserve path relative to EXP_ROOT (so user can see which exp it came from)
    rel="${run#$EXP_ROOT/}"
    dst="$WORKDIR/from_exp_root/$rel"
    mkdir -p "$(dirname "$dst")"
    cp -a "$run" "$dst"
    echo "  + from_exp_root/$rel"
done

for run in "${CENTRAL_RUNS[@]}"; do
    name=$(basename "$run")
    cp -a "$run" "$WORKDIR/from_central/$name"
    echo "  + from_central/$name"
done

# ---- Write a manifest ---------------------------------------------------
cat > "$WORKDIR/MANIFEST.txt" <<EOF
Skywork wandb collection
========================
collected_at:  $(date -u '+%Y-%m-%d %H:%M:%S UTC')
EXP_ROOT:      $EXP_ROOT
CENTRAL_WANDB: $CENTRAL_WANDB
name_pattern:  $NAME_PATTERN

Runs from exp dirs (${#EXP_RUNS[@]}):
$(printf '  %s\n' "${EXP_RUNS[@]}")

Runs from central wandb_offline (${#CENTRAL_RUNS[@]}):
$(printf '  %s\n' "${CENTRAL_RUNS[@]:-(none)}")
EOF

# ---- Bundle ------------------------------------------------------------
log "Bundling into $OUT_ZIP ..."
cd "$WORKDIR"
zip -rq "$OUT_ZIP" .

SIZE=$(du -sh "$OUT_ZIP" | cut -f1)
log "✓ Done: $OUT_ZIP ($SIZE, $TOTAL runs)"
echo
echo "Inspect with:"
echo "  unzip -l $OUT_ZIP | head"
echo "  unzip -p $OUT_ZIP MANIFEST.txt"
echo
echo "Sync to wandb cloud later with:"
echo "  unzip $OUT_ZIP -d \$HOME/skywork_extracted/"
echo "  wandb sync \$HOME/skywork_extracted/from_*/offline-run-*"
