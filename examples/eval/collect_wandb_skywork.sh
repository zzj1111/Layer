#!/usr/bin/env bash
# Find all wandb offline runs under skywork exp root + the central
# wandb_offline cache, bundle into a single zip.
#
# Usage:
#   bash collect_wandb_skywork.sh                                # default paths
#   bash collect_wandb_skywork.sh /custom/exp/root /custom/out.zip
#
# Defaults:
#   OUTPUT_DIR:  /code-fsx/hongpaul-sandbox/code/temp/wandb
#   OUT_ZIP:     $OUTPUT_DIR/skywork_wandb_<ts>.zip
#   STAGE_ROOT:  $OUTPUT_DIR/.staging  (auto-cleaned via trap)
#
# Searches:
#   1. <EXP_ROOT>/**/offline-run-*   (runs co-located with exp dir)
#   2. /checkpoints/hongpaul-sandbox/rl-opt/wandb_offline/wandb/offline-run-*
#      AND filters by run NAME containing "SkyOR1" or "Skywork-OR1"
#      (these were generated by 7B script's WANDB_DIR default)
set -euo pipefail

EXP_ROOT="${1:-/checkpoints/hongpaul-sandbox/rl-opt/skywork/Skywork-OR1-R1Distill7B}"
OUTPUT_DIR="${OUTPUT_DIR:-/code-fsx/hongpaul-sandbox/code/temp/wandb}"
OUT_ZIP="${2:-$OUTPUT_DIR/skywork_wandb_$(date +%Y%m%d_%H%M%S).zip}"
STAGE_ROOT="${STAGE_ROOT:-$OUTPUT_DIR/.staging}"
CENTRAL_WANDB="${CENTRAL_WANDB:-/checkpoints/hongpaul-sandbox/rl-opt/wandb_offline/wandb}"
# extra search roots (training scripts that don't set WANDB_DIR write to cwd)
EXTRA_WANDB_DIRS=(
    "/code-fsx/hongpaul-sandbox/code/temp/Layer/wandb"
    "/code-fsx/hongpaul-sandbox/code/temp/Layer_1/wandb"
)

# Match patterns for filtering central wandb_offline (run name field)
# Override with NAME_PATTERN env; or set FILTER=off to grab ALL runs (no filtering)
NAME_PATTERN="${NAME_PATTERN:-SkyOR1|Skywork-OR1|SkyOR1_R1Distill7B|DeepSeek-R1-Distill-Qwen-7B}"
FILTER="${FILTER:-on}"

log() { echo "[$(date '+%H:%M:%S')] $*" ; }

[ -d "$EXP_ROOT" ] || { echo "ERROR: EXP_ROOT not found: $EXP_ROOT" >&2; exit 1; }

mkdir -p "$OUTPUT_DIR" "$STAGE_ROOT"
WORKDIR=$(mktemp -d -p "$STAGE_ROOT" wandb_collect.XXXXXX)
trap "rm -rf $WORKDIR" EXIT

log "EXP_ROOT: $EXP_ROOT"
log "OUT_ZIP : $OUT_ZIP"
log "Scanning ..."

# ---- Pass 1: runs nested under exp dirs ---------------------------------
log "Pass 1: rglob under EXP_ROOT/**/offline-run-*"
EXP_RUNS=()
while IFS= read -r -d '' d; do
    EXP_RUNS+=("$d")
done < <(find "$EXP_ROOT" -type d -name "offline-run-*" -print0 2>/dev/null)
log "  found ${#EXP_RUNS[@]} runs nested under exp dirs"

# Helper: filter a run dir against NAME_PATTERN. FILTER=off skips all checks.
run_matches() {
    local d="$1"
    if [ "$FILTER" = "off" ]; then return 0; fi
    # Try multiple sources: config.yaml, output.log, wandb-metadata.json, dir name
    local hay=""
    [ -f "$d/files/config.yaml" ] && hay="$hay $(head -c 4096 "$d/files/config.yaml" 2>/dev/null)"
    [ -f "$d/files/output.log" ] && hay="$hay $(head -c 4096 "$d/files/output.log" 2>/dev/null)"
    [ -f "$d/files/wandb-metadata.json" ] && hay="$hay $(cat "$d/files/wandb-metadata.json" 2>/dev/null)"
    hay="$hay $d"
    echo "$hay" | grep -qE "$NAME_PATTERN"
}

scan_central() {
    local root="$1"
    local label="$2"
    if [ ! -d "$root" ]; then
        log "  ($label: dir not present, skipping)"
        return
    fi
    local matched=0
    local total=0
    while IFS= read -r -d '' d; do
        total=$((total + 1))
        if run_matches "$d"; then
            CENTRAL_RUNS+=("$d")
            matched=$((matched + 1))
        fi
    done < <(find "$root" -maxdepth 1 -type d -name "offline-run-*" -print0 2>/dev/null)
    log "  $label: $matched matched / $total total"
}

# ---- Pass 2: central wandb_offline + extra search roots -----------------
log "Pass 2: filtering by NAME_PATTERN='$NAME_PATTERN' (FILTER=$FILTER)"
CENTRAL_RUNS=()
scan_central "$CENTRAL_WANDB" "central wandb_offline"
for extra in "${EXTRA_WANDB_DIRS[@]}"; do
    scan_central "$extra" "extra:$extra"
done

TOTAL=$(( ${#EXP_RUNS[@]} + ${#CENTRAL_RUNS[@]} ))
if [ "$TOTAL" = "0" ]; then
    log "WARNING: no wandb runs found anywhere."
    log "Check that training actually wrote wandb data, or pass a different EXP_ROOT."
    exit 0
fi

log "Total runs to bundle: $TOTAL"

# ---- Stage into workdir preserving useful structure ---------------------
log "Staging into $WORKDIR ..."
mkdir -p "$WORKDIR/from_exp_root" "$WORKDIR/from_central"

for run in "${EXP_RUNS[@]}"; do
    # Preserve path relative to EXP_ROOT (so user can see which exp it came from)
    rel="${run#$EXP_ROOT/}"
    dst="$WORKDIR/from_exp_root/$rel"
    mkdir -p "$(dirname "$dst")"
    cp -a "$run" "$dst"
    echo "  + from_exp_root/$rel"
done

for run in "${CENTRAL_RUNS[@]}"; do
    name=$(basename "$run")
    cp -a "$run" "$WORKDIR/from_central/$name"
    echo "  + from_central/$name"
done

# ---- Write a manifest ---------------------------------------------------
cat > "$WORKDIR/MANIFEST.txt" <<EOF
Skywork wandb collection
========================
collected_at:  $(date -u '+%Y-%m-%d %H:%M:%S UTC')
EXP_ROOT:      $EXP_ROOT
CENTRAL_WANDB: $CENTRAL_WANDB
name_pattern:  $NAME_PATTERN

Runs from exp dirs (${#EXP_RUNS[@]}):
$(printf '  %s\n' "${EXP_RUNS[@]}")

Runs from central wandb_offline (${#CENTRAL_RUNS[@]}):
$(printf '  %s\n' "${CENTRAL_RUNS[@]:-(none)}")
EOF

# ---- Bundle ------------------------------------------------------------
log "Bundling into $OUT_ZIP ..."
cd "$WORKDIR"
zip -rq "$OUT_ZIP" .

SIZE=$(du -sh "$OUT_ZIP" | cut -f1)
log "✓ Done: $OUT_ZIP ($SIZE, $TOTAL runs)"
echo
echo "Inspect with:"
echo "  unzip -l $OUT_ZIP | head"
echo "  unzip -p $OUT_ZIP MANIFEST.txt"
echo
echo "Sync to wandb cloud later with:"
echo "  unzip $OUT_ZIP -d $OUTPUT_DIR/extracted/"
echo "  wandb sync $OUTPUT_DIR/extracted/from_*/offline-run-*"
